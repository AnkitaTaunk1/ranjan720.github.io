$(document).ready(function(){
    
    var value="Total";
    
    $("input:radio[name=crime]").click(function() {
        
        value = $(this).val();
        
        $(".chart").empty();

        var x = d3.scale.ordinal()
            .domain(data.map(function(entry){
              return entry.STATEUT;
            }))
            .rangeBands([0, width]);

        var y = d3.scale.linear()
            .domain([0, d3.max(data, function(d){
              return d[value];
            })])
            .range([height, 0]);

        var ordinalColorScale = d3.scale.category20();

        var xAxis = d3.svg.axis()
              .scale(x)
              .orient("bottom");
        var yAxis = d3.svg.axis()
              .scale(y)
              .orient("left");
        var yGridlines = d3.svg.axis()
                .scale(y)
                .tickSize(-width,0,0)
                .tickFormat("")
                .orient("left");
        var svg = d3.select(".chart").append("svg")
              .attr("id", "chart")
              .attr("width", w)
              .attr("height", h);
        var chart = svg.append("g")
              .classed("display", true)
              .attr("transform", "translate(" + margin.left + "," + margin.top + ")");
        function plot(params){
          this.append("g")
            .call(params.gridlines)
            .classed("gridline", true)
            .attr("transform", "translate(0,0)")
          this.selectAll(".bar")
            .data(params.data)
            .enter()
              .append("rect")
              .classed("bar", true)
              .attr("x", function(d,i){
                return x(d.STATEUT);
              })
              .attr("y", function(d,i){
                return y(d[value]);
              })
              .attr("height", function(d,i){
                return height - y(d[value]);
              })
              .attr("width", function(d){
                return x.rangeBand()-1;
              })
              .style("fill", function(d,i){
                return ordinalColorScale(i);
              });
          this.selectAll(".bar-label")
            .data(params.data)
            .enter()
              .append("text")
              .classed("bar-label", true)
              .attr("x", function(d,i){
                return x(d.STATEUT) + (x.rangeBand()/2)
              })
              .attr("dx", 0)
              .attr("y", function(d,i){
                return y(d[value]);
              })
              .attr("dy", -6)
              .text(function(d){
                return d[value];
              })
          this.append("g")
            .classed("x axis", true)
            .attr("transform", "translate(" + 0 + "," + height + ")")
            .call(params.axis.x)
              .selectAll("text")
                .style("text-anchor", "end")
                .attr("dx", -8)
                .attr("dy", 8)
                .attr("transform", "translate(0,0) rotate(-45)");

          this.append("g")
            .classed("y axis", true)
            .attr("transform", "translate(0,0)")
            .call(params.axis.y);

          this.select(".y.axis")
            .append("text")
            .attr("x", 0)
            .attr("y", 0)
            .style("text-anchor", "middle")
            .attr("transform", "translate(-50," + height/2 + ") rotate(-90)")
            .text("Crime head");

          this.select(".x.axis")
            .append("text")
            .attr("x", 0)
            .attr("y", 0)
            .style("text-anchor", "middle")
            .attr("transform", "translate(" + width/2 + ",80)")
            .text("States and Union Territory");
            
        }
        plot.call(chart, {
            data: $.map(data, function(o,i){
                if (o.YEAR == $("input:radio[name=year]:checked").val())
                    return o;
            }),
          axis:{
            x: xAxis,
            y: yAxis
          },
          gridlines: yGridlines
        });
    });
})